def profile_for(name: str) -> RunProfile:
    profiles = {
        "smoke": RunProfile("smoke", 6, 4, 4, 2, 2, 1, 1, 2, 1, 1.0),
        "full": RunProfile("full", 0, 0, 0, 8, 4, 2, 6, 4, 6, 20.0),
    }
    try:
        return profiles[name]
    except KeyError as exc:
        raise ValueError(f"unknown run profile {name!r}; choose smoke or full") from exc


class DetectAIText(dspy.Signature):
    """Decide whether the supplied passage was generated by an AI."""

    text: str = dspy.InputField(desc="Passage to classify")
    is_ai: bool = dspy.OutputField(desc="True for AI-generated text; otherwise False")


class AIDetector(dspy.Module):
    def __init__(self) -> None:
        super().__init__()
        self.detect = dspy.ChainOfThought(DetectAIText)

    def forward(self, text: str):
        return self.detect(text=text)


def as_bool(value: Any) -> bool:
    if isinstance(value, bool):
        return value
    normalized = str(value).strip().lower()
    if normalized in {"true", "1", "yes"}:
        return True
    if normalized in {"false", "0", "no"}:
        return False
    raise ValueError(f"cannot interpret {value!r} as a boolean")


def exact_match(example: dspy.Example, prediction: Any, trace: Any = None) -> float:
    del trace
    return float(as_bool(prediction.is_ai) == bool(example.is_ai))


def feedback_metric(
    example: dspy.Example,
    prediction: Any,
    trace: Any = None,
    pred_name: str | None = None,
    pred_trace: Any = None,
):
    del trace, pred_name, pred_trace
    score = exact_match(example, prediction)
    if score:
        feedback = "Correct. Preserve the concrete stylistic evidence that supported this decision."
    else:
        feedback = (
            f"Incorrect: the provenance label is is_ai={bool(example.is_ai)}. "
            "Do not equate polish or technical subject matter with AI authorship; inspect phrasing, "
            "rhythm, specificity, and templated transitions together."
        )
    return dspy.Prediction(score=score, feedback=feedback)


def boolean_majority(predictions: list[Any]):
    """Reduce boolean predictions without DSPy's string-only default normalizer."""

    return dspy.majority(predictions, field="is_ai", normalize=lambda value: str(value).lower())


def _limit(values: list[Any], limit: int) -> list[Any]:
    return values[:limit] if limit else values


def load_frozen_examples(
    *, data_path: Path = DATA_PATH, split_path: Path = SPLIT_PATH
) -> dict[str, list[dspy.Example]]:
    manifest = json.loads(split_path.read_text(encoding="utf-8"))
    with data_path.open(newline="", encoding="utf-8") as handle:
        rows = list(csv.DictReader(handle))
    by_pair: dict[str, list[dict[str, str]]] = {}
    for row in rows:
        by_pair.setdefault(row["pair_id"], []).append(row)
    result: dict[str, list[dspy.Example]] = {}
    seen: set[str] = set()
    for split_name, pair_ids in manifest["splits"].items():
        examples: list[dspy.Example] = []
        for pair_id in pair_ids:
            if pair_id in seen:
                raise ValueError(f"pair {pair_id!r} appears in multiple splits")
            seen.add(pair_id)
            pair = by_pair.get(pair_id, [])
            if len(pair) != 2:
                raise ValueError(f"pair {pair_id!r} must contain exactly two rows")
            for row in pair:
                examples.append(
                    dspy.Example(
                        pair_id=row["pair_id"],
                        example_id=row["example_id"],
                        text=row["text"],
                        is_ai=as_bool(row["is_ai"]),
                    ).with_inputs("text")
                )
        result[split_name] = examples
    if seen != set(by_pair):
        raise ValueError("split manifest does not cover every dataset pair")
    return result


def _json_value(value: Any) -> Any:
    if hasattr(value, "toDict"):
        return value.toDict()
    if hasattr(value, "model_dump"):
        return value.model_dump()
    return value


def extract_program_prompts(program: dspy.Module) -> dict[str, Any]:
    prompts: dict[str, Any] = {}
    for name, predictor in program.named_predictors():
        signature = getattr(predictor, "signature", None)
        prompts[name] = {
            "instructions": str(getattr(signature, "instructions", "")),
            "demos": [_json_value(demo) for demo in getattr(predictor, "demos", [])],
        }
    return redact(prompts)


class LMHistoryTracker(BaseCallback):
    """Collect completed calls from every DSPy LM copy used during one run."""

    def __init__(
        self,
        *,
        reflection_model: str,
        ledger: BudgetLedger | None = None,
        per_call_reserve_usd: float = 0.10,
    ) -> None:
        self.reflection_model = reflection_model
        self.ledger = ledger
        self.per_call_reserve_usd = per_call_reserve_usd
        self.history: list[dict[str, Any]] = []
        self._starts: dict[str, tuple[Any, int, float]] = {}
        self._lock = threading.Lock()

    def on_lm_start(self, call_id: str, instance: Any, inputs: dict[str, Any]) -> None:
        del inputs
        with self._lock:
            if self.ledger is not None:
                completed_cost = summarize_history(self.history)["cost_usd"]
                in_flight_reserve = (len(self._starts) + 1) * self.per_call_reserve_usd
                self.ledger.assert_can_spend(completed_cost + in_flight_reserve)
            self._starts[call_id] = (instance, len(instance.history), time.monotonic())

    def on_lm_end(
        self, call_id: str, outputs: dict[str, Any] | None, exception: Exception | None = None
    ) -> None:
        del outputs
        with self._lock:
            started = self._starts.pop(call_id, None)
            if started is None:
                return
            instance, history_start, started_at = started
            new_entries = list(instance.history[history_start:])
            if not new_entries and exception is not None:
                self.history.append(
                    {
                        "model": str(getattr(instance, "model", "unknown")),
                        "experiment_call_id": call_id,
                        "experiment_model_role": "unknown",
                        "experiment_elapsed_seconds": time.monotonic() - started_at,
                        "experiment_exception": f"{type(exception).__name__}: {exception}",
                    }
                )
                return
            for entry in new_entries:
                model = str(entry.get("model") or getattr(instance, "model", "unknown"))
                self.history.append(
                    {
                        **entry,
                        "experiment_call_id": call_id,
                        "experiment_model_role": (
                            "reflection" if model == self.reflection_model else "task"
                        ),
                        "experiment_elapsed_seconds": time.monotonic() - started_at,
                    }
                )

    def snapshot(self) -> int:
        with self._lock:
            return len(self.history)

    def since(self, start: int) -> list[dict[str, Any]]:
        with self._lock:
            return list(self.history[start:])


def hashed_ngram_embeddings(texts: Sequence[str], *, dimensions: int = 512) -> np.ndarray:
    """Create deterministic dependency-free unit vectors for local KNN retrieval."""

    if dimensions < 1:
        raise ValueError("dimensions must be positive")
    matrix = np.zeros((len(texts), dimensions), dtype=np.float32)
    for row_index, text in enumerate(texts):
        tokens = re.findall(r"[A-Za-z0-9_]+", text.lower())
        features = tokens + [f"{left}::{right}" for left, right in zip(tokens, tokens[1:])]
        for feature in features:
            digest = hashlib.sha256(feature.encode("utf-8")).digest()
            column = int.from_bytes(digest[:4], "big") % dimensions
            sign = 1.0 if digest[4] & 1 else -1.0
            matrix[row_index, column] += sign
        norm = float(np.linalg.norm(matrix[row_index]))
        if norm:
            matrix[row_index] /= norm
    return matrix


def _evaluate(
    program: dspy.Module,
    examples: Sequence[dspy.Example],
    *,
    phase: str,
    ledger: BudgetLedger,
    tracker: LMHistoryTracker,
) -> dict[str, Any]:
    predictions: list[dict[str, Any]] = []
    latencies: list[float] = []
    correct = 0
    for example in examples:
        in_flight = summarize_history(tracker.since(0))["cost_usd"]
        ledger.assert_can_spend(in_flight + 0.10)
        started = time.monotonic()
        try:
            prediction = program(**example.inputs())
            predicted = as_bool(prediction.is_ai)
        except Exception as exc:
            status = classify_api_error(exc)
            if is_global_stop_status(status):
                raise
            predictions.append(
                {
                    "phase": phase,
                    "pair_id": example.pair_id,
                    "example_id": example.example_id,
                    "expected_is_ai": bool(example.is_ai),
                    "status": status,
                    "error": f"{type(exc).__name__}: {exc}",
                }
            )
            continue
        elapsed = time.monotonic() - started
        latencies.append(elapsed)
        is_correct = predicted == bool(example.is_ai)
        correct += int(is_correct)
        predictions.append(
            {
                "phase": phase,
                "pair_id": example.pair_id,
                "example_id": example.example_id,
                "expected_is_ai": bool(example.is_ai),
                "predicted_is_ai": predicted,
                "correct": is_correct,
                "latency_seconds": elapsed,
                "output": _json_value(prediction),
                "status": "completed",
            }
        )
    completed = sum(item["status"] == "completed" for item in predictions)
    return {
        "status": "completed" if completed == len(examples) else "failed",
        "score": 100 * correct / len(examples),
        "correct": correct,
        "count": len(examples),
        "latency": summarize_latencies(latencies),
        "predictions": predictions,
    }


def _verify_reload_prediction_parity(
    program: dspy.Module,
    examples: Sequence[dspy.Example],
    recorded_predictions: Sequence[dict[str, Any]],
    *,
    limit: int,
    ledger: BudgetLedger,
    tracker: LMHistoryTracker,
) -> tuple[dict[str, Any], list[dict[str, Any]]]:
    """Compare a reloaded program with recorded labels on a bounded test subset."""

    expected_by_id = {
        item["example_id"]: item.get("predicted_is_ai")
        for item in recorded_predictions
        if item.get("status") == "completed"
    }
    checks: list[dict[str, Any]] = []
    for example in list(examples)[: max(0, limit)]:
        ledger.assert_can_spend(summarize_history(tracker.since(0))["cost_usd"] + 0.10)
        try:
            observed = as_bool(program(**example.inputs()).is_ai)
            expected = expected_by_id[example.example_id]
            checks.append(
                {
                    "phase": "reload_verification",
                    "pair_id": example.pair_id,
                    "example_id": example.example_id,
                    "expected_is_ai": bool(example.is_ai),
                    "recorded_predicted_is_ai": expected,
                    "predicted_is_ai": observed,
                    "prediction_parity": observed == expected,
                    "status": "completed",
                }
            )
        except Exception as exc:
            status = classify_api_error(exc)
            if is_global_stop_status(status):
                raise
            checks.append(
                {
                    "phase": "reload_verification",
                    "pair_id": example.pair_id,
                    "example_id": example.example_id,
                    "expected_is_ai": bool(example.is_ai),
                    "status": status,
                    "error": f"{type(exc).__name__}: {exc}",
                }
            )
    completed = [item for item in checks if item["status"] == "completed"]
    matching = sum(bool(item["prediction_parity"]) for item in completed)
    return (
        {
            "checked": len(checks),
            "completed": len(completed),
            "matching": matching,
            "all_equal": bool(checks) and matching == len(checks),
            "example_ids": [item["example_id"] for item in checks],
        },
        checks,
    )


def _build_optimizer(
    name: str,
    *,
    detector: AIDetector,
    trainset: list[dspy.Example],
    valset: list[dspy.Example],
    task_lm: dspy.LM,
    reflection_lm: dspy.LM,
    profile: RunProfile,
    run_dir: Path,
) -> dspy.Module:
    threads = int(os.getenv("DSPY_NUM_THREADS", "1"))
    if name == "quickstart":
        return detector
    if name == "labeled-few-shot":
        return dspy.LabeledFewShot(k=min(4, len(trainset))).compile(
            detector, trainset=trainset, sample=True
        )
    if name == "bootstrap-few-shot":
        return dspy.BootstrapFewShot(
            metric=exact_match,
            max_bootstrapped_demos=2,
            max_labeled_demos=2,
            max_rounds=1,
            max_errors=1,
        ).compile(detector, trainset=trainset)
    if name == "bootstrap-random-search":
        return dspy.BootstrapFewShotWithRandomSearch(
            metric=exact_match,
            max_bootstrapped_demos=2,
            max_labeled_demos=2,
            max_rounds=1,
            num_candidate_programs=profile.bootstrap_candidates,
            num_threads=threads,
            max_errors=1,
        ).compile(detector, trainset=trainset, valset=valset)
    if name == "knn-few-shot":
        vectorizer = dspy.Embedder(hashed_ngram_embeddings)
        return dspy.KNNFewShot(
            k=min(4, len(trainset)),
            trainset=trainset,
            vectorizer=vectorizer,
            metric=exact_match,
            max_bootstrapped_demos=0,
            max_labeled_demos=min(4, len(trainset)),
            max_rounds=1,
        ).compile(detector)
    if name == "copro":
        return dspy.COPRO(
            prompt_model=reflection_lm,
            metric=exact_match,
            breadth=profile.copro_breadth,
            depth=profile.copro_depth,
            init_temperature=1.0,
            track_stats=True,
        ).compile(
            detector,
            trainset=trainset,
            eval_kwargs={"num_threads": threads, "display_progress": True, "display_table": 0},
        )
    if name == "miprov2":
        return dspy.MIPROv2(
            metric=exact_match,
            prompt_model=reflection_lm,
            task_model=task_lm,
            auto="light",
            max_bootstrapped_demos=2,
            max_labeled_demos=2,
            num_threads=threads,
            max_errors=1,
            seed=42,
            track_stats=True,
            log_dir=str(run_dir / "optimizer_logs"),
        ).compile(
            detector,
            trainset=trainset,
            valset=valset,
            minibatch=True,
            minibatch_size=min(8, len(valset)),
            requires_permission_to_run=False,
        )
    if name == "gepa":
        return dspy.GEPA(
            metric=feedback_metric,
            max_full_evals=profile.gepa_max_full_evals,
            reflection_minibatch_size=min(3, len(trainset)),
            reflection_lm=reflection_lm,
            num_threads=threads,
            use_merge=False,
            track_stats=True,
            track_best_outputs=True,
            log_dir=str(run_dir / "optimizer_logs"),
            seed=42,
            gepa_kwargs={"use_cloudpickle": True},
        ).compile(detector, trainset=trainset, valset=valset)
    if name == "simba":
        return dspy.SIMBA(
            metric=exact_match,
            bsize=min(8, len(trainset)),
            num_candidates=profile.simba_candidates,
            max_steps=profile.simba_steps,
            max_demos=2,
            prompt_model=reflection_lm,
            num_threads=threads,
        ).compile(detector, trainset=trainset, seed=42)
    if name == "ensemble":
        labeled = dspy.LabeledFewShot(k=min(4, len(trainset))).compile(
            detector.deepcopy(), trainset=trainset
        )
        bootstrapped = dspy.BootstrapFewShot(
            metric=exact_match,
            max_bootstrapped_demos=2,
            max_labeled_demos=2,
            max_rounds=1,
            max_errors=1,
        ).compile(detector.deepcopy(), trainset=trainset)
        searched = dspy.BootstrapFewShotWithRandomSearch(
            metric=exact_match,
            max_bootstrapped_demos=2,
            max_labeled_demos=2,
            max_rounds=1,
            num_candidate_programs=profile.bootstrap_candidates,
            num_threads=threads,
            max_errors=1,
        ).compile(detector.deepcopy(), trainset=trainset, valset=valset)
        # size=None evaluates the full fixed pool, avoiding DSPy's unimplemented
        # deterministic sampling path while keeping majority voting reproducible.
        return dspy.Ensemble(reduce_fn=boolean_majority).compile([labeled, bootstrapped, searched])
    raise ValueError(f"optimizer {name!r} is not runnable on this host")


class _Tee(io.TextIOBase):
    def __init__(self, *streams: Any) -> None:
        self.streams = streams

    def write(self, value: str) -> int:
        for stream in self.streams:
            stream.write(value)
            stream.flush()
        return len(value)

    def flush(self) -> None:
        for stream in self.streams:
            stream.flush()


def _git_sha() -> str:
    result = subprocess.run(
        ["git", "rev-parse", "HEAD"], cwd=REPO_ROOT, text=True, capture_output=True, check=False
    )
    return result.stdout.strip() or "unknown"


def _package_versions() -> dict[str, str]:
    packages = ("dspy", "gepa", "openai", "litellm", "pandas", "scikit-learn")
    versions: dict[str, str] = {}
    for package in packages:
        try:
            versions[package] = importlib.metadata.version(package)
        except importlib.metadata.PackageNotFoundError:
            versions[package] = "not-installed"
    return versions


def _artifact_index(run_dir: Path) -> dict[str, str]:
    candidates = {
        "complete_output": "complete_output.txt",
        "lm_history": "lm_history.jsonl",
        "predictions": "predictions.jsonl",
        "program": "program.json",
        "prompts": "prompts.json",
        "metrics": "metrics.json",
    }
    return {
        name: filename
        for name, filename in candidates.items()
        if (run_dir / filename).exists()
    }


def _finalize_preflight_failure(
    *,
    run_dir: Path,
    manifest: dict[str, Any],
    error: BaseException,
) -> tuple[str, str]:
    status = "interrupted" if isinstance(error, KeyboardInterrupt) else classify_api_error(error)
    error_text = f"{type(error).__name__}: {error}"
    metrics = {"status": status, "error": error_text}
    (run_dir / "complete_output.txt").write_text(error_text + "\n", encoding="utf-8")
    write_jsonl(run_dir / "lm_history.jsonl", [])
    write_jsonl(run_dir / "predictions.jsonl", [])
    atomic_write_json(run_dir / "metrics.json", metrics)
    manifest.update(
        {
            "status": status,
            "error": error_text,
            "completed_at": datetime.now(timezone.utc).isoformat(),
            "total_usage": summarize_history([]),
            "artifacts": _artifact_index(run_dir),
        }
    )
    atomic_write_json(run_dir / "manifest.json", manifest)
    return status, error_text


def run_experiment(
    optimizer_name: str,
    *,
    profile_name: str | None = None,
    task_model: str | None = None,
    reflection_model: str | None = None,
) -> dict[str, Any]:
    if optimizer_name not in OPTIMIZER_NAMES:
        raise ValueError(f"unknown optimizer {optimizer_name!r}")
    profile = profile_for(profile_name or os.getenv("CHAPTER06_PROFILE") or "smoke")
    load_dotenv(REPO_ROOT / ".env")
    task_model = task_model or os.getenv("TASK_MODEL") or "openai/gpt-5.6-luna"
    reflection_model = reflection_model or os.getenv(
        "REFLECTION_MODEL"
    ) or "openai/gpt-5.6-sol"
    request_timeout_seconds = float(os.getenv("CHAPTER06_REQUEST_TIMEOUT_SECONDS", "120"))
    num_retries = int(os.getenv("CHAPTER06_NUM_RETRIES", "1"))
    parity_limit = int(os.getenv("CHAPTER06_PARITY_LIMIT", "3"))
    run_id = datetime.now(timezone.utc).strftime("%Y%m%dT%H%M%S.%fZ")
    run_dir = RESULTS_ROOT / profile.name / optimizer_name / run_id
    run_dir.mkdir(parents=True, exist_ok=False)
    manifest = {
        "schema_version": 1,
        "run_id": run_id,
        "optimizer": optimizer_name,
        "profile": asdict(profile),
        "task_model": task_model,
        "reflection_model": reflection_model,
        "request_timeout_seconds": request_timeout_seconds,
        "num_retries": num_retries,
        "reload_prediction_parity_limit": parity_limit,
        "started_at": datetime.now(timezone.utc).isoformat(),
        "git_sha": _git_sha(),
        "packages": _package_versions(),
        "platform": {"system": platform.system(), "machine": platform.machine()},
        "status": "running",
    }
    atomic_write_json(run_dir / "manifest.json", manifest)

    try:
        manifest.update(
            {
                "dataset_manifest": json.loads(SPLIT_PATH.read_text(encoding="utf-8")),
                "split_manifest_sha256": hashlib.sha256(SPLIT_PATH.read_bytes()).hexdigest(),
            }
        )
        atomic_write_json(run_dir / "manifest.json", manifest)
    except BaseException as exc:
        _finalize_preflight_failure(run_dir=run_dir, manifest=manifest, error=exc)
        raise

    if optimizer_name in GPU_OPTIMIZERS:
        manifest.update(
            {
                "status": "hardware_blocked",
                "reason": (
                    "Requires an NVIDIA CUDA GPU and local trainable model; "
                    f"this host is {platform.system()} {platform.machine()}."
                ),
                "completed_at": datetime.now(timezone.utc).isoformat(),
            }
        )
        atomic_write_json(run_dir / "metrics.json", {"status": "hardware_blocked"})
        manifest["artifacts"] = _artifact_index(run_dir)
        atomic_write_json(run_dir / "manifest.json", manifest)
        return manifest

    ledger: BudgetLedger | None = None
    try:
        ledger = BudgetLedger()
        if not os.getenv("OPENAI_API_KEY"):
            raise EnvironmentError("Set OPENAI_API_KEY in the repository .env file")
        ledger.assert_can_spend(profile.projected_stage_cost_usd)
        datasets = load_frozen_examples()
        trainset = _limit(datasets["train"], profile.train_limit)
        valset = _limit(datasets["validation"], profile.validation_limit)
        testset = _limit(datasets["test"], profile.test_limit)
        task_lm = dspy.LM(
            task_model,
            num_retries=num_retries,
            cache=False,
            timeout=request_timeout_seconds,
        )
        reflection_lm = dspy.LM(
            reflection_model,
            num_retries=num_retries,
            cache=False,
            timeout=request_timeout_seconds,
        )
        tracker = LMHistoryTracker(reflection_model=reflection_model, ledger=ledger)
        dspy.configure(lm=task_lm, callbacks=[tracker])
        detector = AIDetector()
        detector.set_lm(task_lm)
        run_history_start = tracker.snapshot()
    except BaseException as exc:
        status, _ = _finalize_preflight_failure(
            run_dir=run_dir,
            manifest=manifest,
            error=exc,
        )
        if ledger is not None:
            ledger.record(
                stage=f"optimizer_{profile.name}",
                run_id=f"{optimizer_name}-{profile.name}-{run_id}",
                cost_usd=0.0,
                metadata={"optimizer": optimizer_name, "profile": profile.name, "status": status},
            )
        raise

    status = "completed"
    error = ""
    metrics: dict[str, Any] = {}
    all_predictions: list[dict[str, Any]] = []
    console_path = run_dir / "complete_output.txt"

    try:
        with console_path.open("w", encoding="utf-8") as console, contextlib.redirect_stdout(
            _Tee(sys.stdout, console)  # type: ignore[type-var]
        ), contextlib.redirect_stderr(
            _Tee(sys.stderr, console)  # type: ignore[type-var]
        ):
            print(
                json.dumps(
                    {
                        "run_id": run_id,
                        "optimizer": optimizer_name,
                        "profile": profile.name,
                        "train": len(trainset),
                        "validation": len(valset),
                        "test": len(testset),
                    },
                    indent=2,
                )
            )
            baseline_history_start = tracker.snapshot()
            baseline = _evaluate(
                detector,
                testset,
                phase="baseline",
                ledger=ledger,
                tracker=tracker,
            )
            baseline_history = tracker.since(baseline_history_start)
            all_predictions = baseline["predictions"]
            if baseline["status"] != "completed":
                first_error = next(
                    item.get("error", "unknown evaluation error")
                    for item in baseline["predictions"]
                    if item["status"] != "completed"
                )
                raise RuntimeError(f"baseline evaluation failed: {first_error}")
            print(f"Baseline score: {baseline['score']:.2f}%")
            if optimizer_name == "quickstart":
                optimized_detector = detector
                optimized = baseline
                optimization_seconds = 0.0
                optimization_history: list[dict[str, Any]] = []
                optimized_history: list[dict[str, Any]] = []
            else:
                optimization_history_start = tracker.snapshot()
                optimization_started = time.monotonic()
                optimized_detector = _build_optimizer(
                    optimizer_name,
                    detector=detector,
                    trainset=trainset,
                    valset=valset,
                    task_lm=task_lm,
                    reflection_lm=reflection_lm,
                    profile=profile,
                    run_dir=run_dir,
                )
                optimization_seconds = time.monotonic() - optimization_started
                optimization_history = tracker.since(optimization_history_start)
                optimized_history_start = tracker.snapshot()
                optimized = _evaluate(
                    optimized_detector,
                    testset,
                    phase="optimized",
                    ledger=ledger,
                    tracker=tracker,
                )
                optimized_history = tracker.since(optimized_history_start)
                all_predictions = baseline["predictions"] + optimized["predictions"]
                if optimized["status"] != "completed":
                    first_error = next(
                        item.get("error", "unknown evaluation error")
                        for item in optimized["predictions"]
                        if item["status"] != "completed"
                    )
                    raise RuntimeError(f"optimized evaluation failed: {first_error}")
            program_path = run_dir / "program.json"
            optimized_detector.save(program_path, save_program=False)
            final_prompts = extract_program_prompts(optimized_detector)
            reloaded_detector = optimized_detector.deepcopy()
            reloaded_detector.load(program_path)
            reload_prompt_parity = extract_program_prompts(reloaded_detector) == final_prompts
            if not reload_prompt_parity:
                raise RuntimeError("serialized program did not preserve the final prompts on reload")
            reload_history_start = tracker.snapshot()
            reload_prediction_parity, reload_predictions = _verify_reload_prediction_parity(
                reloaded_detector,
                testset,
                optimized["predictions"],
                limit=parity_limit,
                ledger=ledger,
                tracker=tracker,
            )
            reload_history = tracker.since(reload_history_start)
            all_predictions.extend(reload_predictions)
            atomic_write_json(run_dir / "prompts.json", final_prompts)
            metrics = {
                "status": "completed",
                "optimizer": optimizer_name,
                "profile": profile.name,
                "baseline_score": baseline["score"],
                "optimized_score": optimized["score"],
                "uplift_points": optimized["score"] - baseline["score"],
                "optimization_seconds": optimization_seconds,
                "baseline_latency": baseline["latency"],
                "optimized_latency": optimized["latency"],
                "baseline_usage": summarize_history(baseline_history),
                "optimization_usage": summarize_history(optimization_history),
                "optimized_evaluation_usage": summarize_history(optimized_history),
                "reload_verification_usage": summarize_history(reload_history),
                "reload_prompt_parity": reload_prompt_parity,
                "reload_prediction_parity": reload_prediction_parity,
            }
            print(f"Optimized score: {optimized['score']:.2f}%")
            print(f"Uplift: {metrics['uplift_points']:+.2f} points")
            print(f"Optimization time: {optimization_seconds:.2f}s")
            print(json.dumps(metrics, indent=2))
    except BaseException as exc:
        status = "interrupted" if isinstance(exc, KeyboardInterrupt) else classify_api_error(exc)
        error = f"{type(exc).__name__}: {exc}"
        if status == "failed":
            status = "failed"
        metrics = {"status": status, "error": error}
        raise
    finally:
        history = tracker.since(run_history_start)
        usage = summarize_history(history)
        write_jsonl(run_dir / "lm_history.jsonl", history)
        write_jsonl(run_dir / "predictions.jsonl", all_predictions)
        atomic_write_json(run_dir / "metrics.json", metrics)
        manifest.update(
            {
                "status": status,
                "error": error,
                "completed_at": datetime.now(timezone.utc).isoformat(),
                "total_usage": usage,
                "artifacts": _artifact_index(run_dir),
            }
        )
        atomic_write_json(run_dir / "manifest.json", manifest)
        ledger.record(
            stage=f"optimizer_{profile.name}",
            run_id=f"{optimizer_name}-{profile.name}-{run_id}",
            cost_usd=usage["cost_usd"],
            metadata={"optimizer": optimizer_name, "profile": profile.name, "status": status},
        )
    return {"run_dir": str(run_dir), **metrics}


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("optimizer", choices=OPTIMIZER_NAMES)
    parser.add_argument("--profile", choices=("smoke", "full"), default=None)
    return parser


def main(argv: Sequence[str] | None = None) -> int:
    args = build_parser().parse_args(argv)
    result = run_experiment(args.optimizer, profile_name=args.profile)
    print(json.dumps(result, indent=2))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
