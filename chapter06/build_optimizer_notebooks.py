def optimizer_notebook(
    title: str,
    description: str,
    optimizer_code: str,
    *,
    extra_cells: list[dict] | None = None,
    install: dict = INSTALL,
    result: dict = RESULT,
) -> dict:
    cells = [
        markdown(
            f"""
            # {title}

            {description}

            This notebook is self-contained and targets the stable DSPy 3.x API pinned by
            `requirements.txt`. Its default data and optimizer budgets are smoke-test sized;
            set `TRAIN_LIMIT=0`, `VAL_LIMIT=0`, and `EVAL_LIMIT=0` for the complete split.
            """
        ),
        install,
        COMMON_SETUP,
        COMMON_PROGRAM,
        BASELINE,
    ]
    if extra_cells:
        cells.extend(extra_cells)
    cells.extend([code(optimizer_code), result])
    return notebook(cells)


