"""Generate the self-contained Chapter 6 notebooks.

The notebooks intentionally duplicate their setup cells so a reader can open any
one of them and run it top-to-bottom. Keeping those cells in this generator makes
the duplicated code reviewable and prevents optimizer examples from drifting.
"""

