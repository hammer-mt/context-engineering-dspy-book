NOTEBOOKS["bootstrap-finetune.ipynb"] = notebook(
    [
        markdown(
            """
            # BootstrapFinetune (CUDA)

            `BootstrapFinetune` trains model weights from successful DSPy traces. GPT-5.6
            Luna and Sol do not support fine-tuning, so this notebook uses Sol as the
            teacher and a local Qwen model as the trainable student. It requires an NVIDIA
            CUDA GPU and SGLang; the notebook fails early with a clear hardware message.
            """
        ),
        FINETUNE_INSTALL,
        COMMON_SETUP,
        COMMON_PROGRAM,
        code(
            r'''
            import torch
            from dspy.clients.lm_local import LocalProvider

            if not torch.cuda.is_available():
                raise RuntimeError("BootstrapFinetune requires an NVIDIA CUDA GPU. Use finetune-mac-m3.ipynb on Apple Silicon.")

            LOCAL_MODEL = os.getenv("LOCAL_MODEL", "Qwen/Qwen2.5-1.5B-Instruct")
            local_lm = dspy.LM(
                f"openai/local:{LOCAL_MODEL}",
                provider=LocalProvider(),
                max_tokens=512,
            )

            student = AIDetector()
            student.set_lm(local_lm)
            teacher = AIDetector()
            teacher.set_lm(reflection_lm)

            optimizer = dspy.BootstrapFinetune(
                metric=exact_match,
                multitask=False,
                train_kwargs={
                    "num_train_epochs": 1,
                    "per_device_train_batch_size": 1,
                    "gradient_accumulation_steps": 4,
                    "learning_rate": 2e-5,
                    "max_seq_length": 1_024,
                    "device": "cuda",
                    "use_peft": True,
                },
            )

            # teacher is a DSPy Module, not an LM. Each student predictor already has local_lm assigned.
            optimized_detector = optimizer.compile(
                student,
                trainset=trainset,
                teacher=teacher,
            )
            ''',
        ),
        code(
            """
            # BootstrapFinetune returns the trained model but does not launch its
            # local inference server. Launch once for evaluation, then clean up.
            optimized_lms = {predictor.lm for predictor in optimized_detector.predictors()}
            for lm in optimized_lms:
                lm.launch()
            try:
                optimized_score = evaluate(optimized_detector)
                print(f"Fine-tuned score: {optimized_score}")
            finally:
                for lm in optimized_lms:
                    lm.kill()
            """
        ),
    ]
)


NOTEBOOKS["better-together.ipynb"] = notebook(
    [
        markdown(
            """
            # BetterTogether (CUDA)

            `BetterTogether` alternates prompt optimization (`p`) and weight optimization
            (`w`). The keyword names passed to the constructor define the strategy symbols;
            this notebook therefore uses `p=...`, `w=...` and `strategy="p -> w -> p"`.
            It requires the same CUDA/SGLang stack as `BootstrapFinetune`.
            """
        ),
        FINETUNE_INSTALL,
        COMMON_SETUP,
        COMMON_PROGRAM,
        code(
            r'''
            import torch
            from dspy.clients.lm_local import LocalProvider

            if not torch.cuda.is_available():
                raise RuntimeError("BetterTogether weight optimization requires an NVIDIA CUDA GPU.")

            LOCAL_MODEL = os.getenv("LOCAL_MODEL", "Qwen/Qwen2.5-1.5B-Instruct")
            local_lm = dspy.LM(
                f"openai/local:{LOCAL_MODEL}",
                provider=LocalProvider(),
                max_tokens=512,
            )
            student = AIDetector()
            student.set_lm(local_lm)

            prompt_optimizer = dspy.MIPROv2(
                metric=exact_match,
                prompt_model=reflection_lm,
                task_model=local_lm,
                auto="light",
                max_bootstrapped_demos=2,
                max_labeled_demos=2,
                num_threads=NUM_THREADS,
            )
            weight_optimizer = dspy.BootstrapFinetune(
                metric=exact_match,
                multitask=False,
                train_kwargs={
                    "num_train_epochs": 1,
                    "per_device_train_batch_size": 1,
                    "gradient_accumulation_steps": 4,
                    "max_seq_length": 1_024,
                    "device": "cuda",
                    "use_peft": True,
                },
            )

            optimizer = dspy.BetterTogether(
                metric=exact_match,
                p=prompt_optimizer,
                w=weight_optimizer,
            )
            optimized_detector = optimizer.compile(
                student,
                trainset=trainset,
                valset=valset,
                strategy="p -> w -> p",
            )
            ''',
        ),
        code(
            """
            # BetterTogether cleans up local servers before returning.
            optimized_lms = {predictor.lm for predictor in optimized_detector.predictors()}
            for lm in optimized_lms:
                lm.launch()
            try:
                optimized_score = evaluate(optimized_detector)
                print(f"BetterTogether score: {optimized_score}")
            finally:
                for lm in optimized_lms:
                    lm.kill()
            """
        ),
    ]
)


