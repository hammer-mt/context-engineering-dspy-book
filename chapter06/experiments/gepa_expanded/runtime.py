class DetectAIText(dspy.Signature):
    """Decide whether the supplied passage was generated by an AI."""

    text: str = dspy.InputField(desc="Passage to classify")
    is_ai: bool = dspy.OutputField(desc="True for AI-generated text; otherwise False")


class AIDetector(dspy.Module):
    def __init__(self) -> None:
        super().__init__()
        self.detect = dspy.ChainOfThought(DetectAIText)

    def forward(self, text: str):
        return self.detect(text=text)


def exact_match(example: dspy.Example, prediction: Any, trace: Any = None) -> float:
    del trace
    return float(as_bool(prediction.is_ai) == bool(example.is_ai))


def feedback_metric(
    example: dspy.Example,
    prediction: Any,
    trace: Any = None,
    pred_name: str | None = None,
    pred_trace: Any = None,
):
    del trace, pred_name, pred_trace
    score = exact_match(example, prediction)
    if score:
        feedback = (
            "Correct. Preserve the concrete textual evidence behind this decision, including "
            "sentence rhythm, source-specific detail, transitions, and whether the prose feels "
            "locally authored or globally smoothed."
        )
    else:
        feedback = (
            f"Incorrect: the provenance label is is_ai={bool(example.is_ai)}. Technical polish is "
            "not sufficient evidence of AI authorship. Contrast irregular human documentation "
            "details (versioned names, abrupt qualifications, direct commands, uneven cadence) "
            "with model-rewrite tendencies (balanced clauses, explanatory restatement, abstract "
            "benefits, tidy transitions, and a self-contained concluding sentence)."
        )
    return dspy.Prediction(score=score, feedback=feedback)


def load_project_env() -> Path:
    candidates: list[Path] = []
    explicit = os.getenv("CHAPTER06_ENV_FILE")
    if explicit:
        candidates.append(Path(explicit).expanduser())
    try:
        common = subprocess.check_output(
            ["git", "rev-parse", "--git-common-dir"], text=True
        ).strip()
        common_path = Path(common)
        if not common_path.is_absolute():
            common_path = Path.cwd() / common_path
        candidates.append(common_path.resolve().parent / ".env")
    except (OSError, subprocess.CalledProcessError):
        pass
    candidates.append(Path.cwd() / ".env")
    for candidate in candidates:
        if candidate.exists():
            load_dotenv(candidate)
            if os.getenv("OPENAI_API_KEY"):
                os.environ.setdefault("DSPY_CACHEDIR", str(RESULTS_DIR / ".dspy_cache"))
                return candidate
    raise EnvironmentError("OPENAI_API_KEY was not found in the worktree or primary repo .env")


def make_lm(model: str, *, cache: bool = False, max_tokens: int | None = None) -> dspy.LM:
    kwargs: dict[str, Any] = {
        "num_retries": int(os.getenv("CHAPTER06_NUM_RETRIES", "1")),
        "cache": cache,
        "timeout": float(os.getenv("CHAPTER06_REQUEST_TIMEOUT_SECONDS", "120")),
    }
    if max_tokens is not None:
        kwargs["max_tokens"] = max_tokens
    return dspy.LM(model, **kwargs)


def new_run_id(stage: str) -> str:
    stamp = datetime.now(timezone.utc).strftime("%Y%m%dT%H%M%S.%fZ")
    return f"{stage}-{stamp}"


def history_cost(lm: dspy.LM) -> float:
    return float(summarize_history(lm.history)["cost_usd"])


def assert_stage_budget(ledger: BudgetLedger, lm: dspy.LM, *, stage_cap: float) -> None:
    spent = history_cost(lm)
    if spent >= stage_cap:
        raise RuntimeError(f"stage cost ${spent:.4f} reached its ${stage_cap:.2f} cap")
    ledger.assert_can_spend(spent + min(0.10, stage_cap - spent))


def finish_stage(
    *,
    ledger: BudgetLedger,
    lm: dspy.LM,
    stage: str,
    run_id: str,
    status: str,
    artifact_dir: Path,
    metadata: dict[str, Any] | None = None,
) -> dict[str, Any]:
    artifact_dir.mkdir(parents=True, exist_ok=True)
    summary = summarize_history(lm.history)
    write_jsonl(artifact_dir / "lm_history.jsonl", lm.history)
    ledger.record(
        stage=stage,
        run_id=run_id,
        cost_usd=summary["cost_usd"],
        metadata={"status": status, "summary": summary, **(metadata or {})},
    )
    return summary


def predict_record(
    detector: dspy.Module,
    *,
    text: str,
    expected: bool,
    identity: dict[str, Any],
) -> dict[str, Any]:
    started = time.monotonic()
    try:
        prediction = detector(text=text)
        predicted = as_bool(prediction.is_ai)
        return {
            **identity,
            "status": "completed",
            "expected_is_ai": expected,
            "predicted_is_ai": predicted,
            "correct": int(predicted == expected),
            "reasoning": str(getattr(prediction, "reasoning", "")),
            "latency_seconds": time.monotonic() - started,
        }
    except Exception as exc:
        return {
            **identity,
            "status": classify_api_error(exc),
            "expected_is_ai": expected,
            "error": f"{type(exc).__name__}: {exc}",
            "latency_seconds": time.monotonic() - started,
        }


def evaluate_program(
    program: dspy.Module,
    examples: Sequence[dspy.Example],
    *,
    predictions_path: Path,
    stop_on_global_error: bool = True,
) -> dict[str, Any]:
    records: list[dict[str, Any]] = []
    for example in examples:
        record = predict_record(
            program,
            text=str(example.text),
            expected=bool(example.is_ai),
            identity={
                "pair_id": str(example.pair_id),
                "example_id": str(example.example_id),
            },
        )
        append_jsonl(predictions_path, record)
        records.append(record)
        if stop_on_global_error and is_global_stop_status(record["status"]):
            raise RuntimeError(record.get("error", record["status"]))
    completed = [record for record in records if record["status"] == "completed"]
    if len(completed) != len(examples):
        raise RuntimeError(f"only {len(completed)}/{len(examples)} predictions completed")
    latencies = [float(record["latency_seconds"]) for record in completed]
    correct = sum(int(record["correct"]) for record in completed)
    return {
        "row_count": len(completed),
        "correct": correct,
        "accuracy_pct": 100 * correct / len(completed),
        "latency": summarize_latencies(latencies),
        "predictions": completed,
    }
